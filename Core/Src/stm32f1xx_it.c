/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
  if (TIM3_counter % (1000*diffcu) == 0)
  {
    fSnakeMove = 1;
  }

  if(TIM3_counter>100000)
  {
    TIM3_counter = 1;
  }
  TIM3_counter++;
  /* USER CODE END TIM3_IRQn 1 */
}

/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
  if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) != RESET)
  {
    __HAL_UART_CLEAR_IDLEFLAG(&huart1);                                           //清空空闲标志位
    HAL_UART_DMAStop(&huart1);                                                    //停止DMA接收中断
    UART1_DMA_RX_len = UART1_MAX_buffer - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx); //计算接收数据长度
    HAL_UART_Transmit_DMA(&huart1, UART1_DMA_RX_buffer, UART1_DMA_RX_len);//将接收的数据回发出去
    printf("|||%X %X %X|||\r\n",UART1_DMA_RX_buffer[0],UART1_DMA_RX_buffer[1],UART1_DMA_RX_buffer[2]);
    for(uint8_t i=0;i<3;i++)
    {
      RawKey[i] = UART1_DMA_RX_buffer[i];
    }
    fRecive_key = 1;
    HAL_UART_Receive_DMA(&huart1, UART1_DMA_RX_buffer, sizeof(UART1_DMA_RX_buffer)); //开启DMA接收中断
  }
  /* USER CODE END USART1_IRQn 1 */
}

